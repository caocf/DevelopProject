public class ChildrenModel {
	private Boolean isAdd;
	private String hukouFirst;
	private String hukouSecond;
	private String chusheng;
	private String zhunsheng;
	
	public Boolean getIsAdd() {
		return isAdd;
	}
	public void setIsAdd(Boolean isAdd) {
		this.isAdd = isAdd;
	}
	public String getHukouFirst() {
		return hukouFirst;
	}
	public void setHukouFirst(String hukouFirst) {
		this.hukouFirst = hukouFirst;
	}
	public String getHukouSecond() {
		return hukouSecond;
	}
	public void setHukouSecond(String hukouSecond) {
		this.hukouSecond = hukouSecond;
	}
	public String getChusheng() {
		return chusheng;
	}
	public void setChusheng(String chusheng) {
		this.chusheng = chusheng;
	}
	public String getZhunsheng() {
		return zhunsheng;
	}
	public void setZhunsheng(String zhunsheng) {
		this.zhunsheng = zhunsheng;
	}
}
