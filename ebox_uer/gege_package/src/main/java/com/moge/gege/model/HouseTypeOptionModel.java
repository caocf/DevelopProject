package com.moge.gege.model;

public class HouseTypeOptionModel extends BaseOptionModel
{
    int room;
    int hall;
    int washroom;

    public int getRoom()
    {
        return room;
    }

    public void setRoom(int room)
    {
        this.room = room;
    }

    public int getHall()
    {
        return hall;
    }

    public void setHall(int hall)
    {
        this.hall = hall;
    }

    public int getWashroom()
    {
        return washroom;
    }

    public void setWashroom(int washroom)
    {
        this.washroom = washroom;
    }
}
