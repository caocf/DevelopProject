package com.moge.gege.model;

public class UserStatModel
{
    private int friend_count;
    private int neighbor_count;
    private int gift_count;
    private int integration;

    public int getFriend_count()
    {
        return friend_count;
    }

    public void setFriend_count(int friend_count)
    {
        this.friend_count = friend_count;
    }

    public int getNeighbor_count()
    {
        return neighbor_count;
    }

    public void setNeighbor_count(int neighbor_count)
    {
        this.neighbor_count = neighbor_count;
    }

    public int getGift_count()
    {
        return gift_count;
    }

    public void setGift_count(int gift_count)
    {
        this.gift_count = gift_count;
    }

    public int getIntegration()
    {
        return integration;
    }

    public void setIntegration(int integration)
    {
        this.integration = integration;
    }

}
